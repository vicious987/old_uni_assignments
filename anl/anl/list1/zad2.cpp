#include <stdio.h>
#include <cmath>

double recsec(int n) {
	if(n == 0) return 1;
	if(n == 1)
		return -1.0 / 7.0;
	else
		return 13.0 / 7.0 * recsec(n-1) + 2.0 / 7.0 * recsec(n-2);
}

double alt_way(int n) {
	return pow( (-1.0 / 7.0), n);
}



int main()
{
	for(int i = 0; i < 20; i++)
		printf("% .20f --- % .20f %d\n", recsec(i), alt_way(i), i);
	
}
